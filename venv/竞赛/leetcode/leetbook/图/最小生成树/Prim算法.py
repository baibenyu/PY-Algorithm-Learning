import time
# 切分定理:将一个连通分量任意切为两个连通分量,其中连接两个分量的路径中最短的那一条一定处于最小生成树中
# 步骤:1--准备两个集合,其中一者仅包含已访问的结点,另一者仅包含未访问的结点,随机取未访问的结点作为初始连通分量
#     2--遍历连通分量中所有结点的边,选择权值最小的边,将边对应的终点结点加入已访问集合,并加入连通分量
#     3--重复2直至所有结点都被访问过
if __name__ == '__main__':
    start = time.perf_counter()

    end = time.perf_counter()
    print(end - start)
